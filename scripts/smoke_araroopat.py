def main() -> int:
    print("=" * 70)
    print("AraRooPat smoke test")
    print("=" * 70)

    # Train.
    cache_dir = Path("outputs/tokenizers/araroopat_smoke_cache")
    save_dir = Path("outputs/tokenizers/araroopat_smoke")

    print(f"\n[1/4] Training on {len(CORPUS)} sentences...")
    tok = AraRooPatTokenizer(
        max_roots=2000,
        max_patterns=200,
        min_root_freq=1,
        min_pattern_freq=1,
        generator_timeout_ms=200,
        cache_corpus_analysis=True,
    )
    tok.train(CORPUS, vocab_size=None, cache_path=str(cache_dir))
    print(f"   Vocab size: {tok.vocab_size}")
    print(f"   Reconstruction entries: {len(tok._reconstruction)}")

    # Save.
    print(f"\n[2/4] Saving to {save_dir}...")
    tok.save(save_dir)
    files = sorted(p.name for p in save_dir.iterdir())
    print(f"   Files: {files}")

    # Load + roundtrip.
    print(f"\n[3/4] Roundtrip test:")
    tok2 = AraRooPatTokenizer()
    tok2.load(save_dir)
    test_cases = [
        "كتب الطالب الدرس",
        "والكتاب الذي قرأته",
        "قال الرجل للولد",       # weak root (قول → قال)
        "يدرس الطلاب",
        "تلفزيون الأطفال ممتع",   # loanword
        "في عام 2024",            # mixed digits
        "السلام عليكم!",          # punctuation
    ]
    for text in test_cases:
        enc = tok2.encode(text)
        dec = tok2.decode(enc.input_ids)
        print(f"   IN : {text}")
        print(f"   OUT: {dec}")
        print(f"   IDs: {len(enc.input_ids)}, tokens: {enc.tokens[:8]}")
        print()

    # Intrinsic + morphological metrics on the corpus.
    print("[4/4] Intrinsic + morphological metrics:")
    metrics = compute_intrinsic_metrics(
        tok2,
        CORPUS,
        morphological_metrics=True,
        morph_sample_size=80,
    )
    print(json.dumps(metrics, ensure_ascii=False, indent=2))

    return 0


if __name__ == "__main__":
    sys.exit(main())
